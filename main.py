name = 'Университет' # заданное значение
print(name[0])       # вывод первого символа
print(name[-1])      # вывод последнего символа
print(name[6::])     # вывод последней части строки с нечетным количеством символов
print(name[::-1])    # вывод строки наоборот
print(name[1::2])    # вывод каждого второго символа